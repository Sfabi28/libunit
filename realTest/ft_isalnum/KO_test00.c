#include "../realtest.h"

int KO_test00(void)
{
	int arr[128];
	int i = 0;
	int j = 0;

	while (i < 128)
	{
		if (!((i >= 'a' && i <= 'z') ||
			(i >= 'A' && i <= 'Z') ||
			(i >= '0' && i <= '9')))
			arr[j++] = i;
		i++;
	}
	i = 0;
	while (--j >= 0)
	{
		if (ft_isalnum(arr[j]) != 0)
			return -1;
	}
	return 0;
}

#include "../../libunit.h"

int	OK_test01()
{
	int arr[128];
	int i = 0;
	int j = 0;

	while (i < 128)
	{
		if (i < '0' || i > '9')
			arr[j++] = i;
		i++;
	}
	i = 0;
	while (i < 128)
	{
		if (ft_isdigit(arr[i]) != 0)
			return -1;
		i++;
	}
	return 0;
}

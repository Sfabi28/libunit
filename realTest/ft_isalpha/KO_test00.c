#include "../../libunit.h"

int	KO_test00()
{
	int arr[128];
	int i = 0;
	int j = 0;

	while (i < 128)
	{
		if ((i < 'a' && i > 'z') || (i < 'A' && i > 'Z'))
			arr[j++] = i;
		i++;
	}
	i = 0;
	while (i < 128)
	{
		if (ft_isalpha(arr[i]) != 0)
			return -1;
		i++;
	}
	return 0;
}

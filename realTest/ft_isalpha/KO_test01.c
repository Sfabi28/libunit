#include "../realtest.h"

int	ko_test01(void)
{
	int	arr[128];
	int	i;
	int	j;

	j = 0;
	i = 0;
	while (i < 128)
	{
		if (!((i >= 'a' && i <= 'z') || (i >= 'A' && i <= 'Z')))
			arr[j++] = i;
		i++;
	}
	while (--j >= 0)
	{
		if (ft_isalpha(arr[j]) != 0)
			return (-1);
	}
	return (0);
}

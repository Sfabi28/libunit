#include "../realtest.h"

int	OK_test03(void)
{
	int arr[128];
	int i = 0;
	int j = 0;

	while (i < 128)
	{
		if (i >= 32 && i <= 126)
			arr[j++] = i;
		i++;
	}
	i = 0;
	while (i < 128)
	{
		if (ft_isprint(arr[i]) != 0)
			return -1;
		i++;
	}
	return 0;
}

#include "../realtest.h"

int	OK_test03(void)
{
	int arr[128];
	int i = 0;
	int j = 0;

	while (i < 128)
	{
		if (i >= ' ' && i <= '~')
			arr[j++] = i;
		i++;
	}
	while (--j >= 0)
	{
		if (ft_isprint(arr[j]) != 1)
			return -1;
	}
	return 0;
}
